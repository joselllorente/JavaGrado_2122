package edu.ucjc.gradoinformatica.programacion.clases.ejercicios.ordenador;

public class MemoriaRAM {
	String marca;
	String tipo; 
	int capacidad;
	double frecuencia;
	double precio;
	
	
	public MemoriaRAM(String marca, String tipo, int capacidad, double frecuencia, double precio) {
		this.marca = marca;
		this.tipo = tipo;
		this.capacidad = capacidad;
		this.frecuencia = frecuencia;
		this.precio = precio;
	}
	
	
}
