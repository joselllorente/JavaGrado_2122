package edu.ucjc.gradoinformatica.programacion.clases.ejercicios.ordenador;

public class DiscoDuro {
	String marca; 
	String modelo; 
	String tipo; 
	int capacidad; 
	double precio;
	
	public DiscoDuro(String marca, String modelo, String tipo, int capacidad, double precio) {
		this.marca = marca;
		this.modelo = modelo;
		this.tipo = tipo;
		this.capacidad = capacidad;
		this.precio = precio;
	}
	
	
}
